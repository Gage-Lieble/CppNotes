#include <iostream> 
using namespace std;

int main() {
    cout << "Welcome to c++! " << endl;
    cout << "Hello Gage! You're " << 20 << " years old." << endl; // << : concat print

    cout << "Instead of using 2 seperate cout's like above" << endl << "you can combine them into 1 single cout with more <<'s." << endl << "endl's can be scattered throughout 1 cout!";
    return 0;
}

